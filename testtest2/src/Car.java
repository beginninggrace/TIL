public class Car {
    public String brand; // 회사 브랜드
    public double topSpeed; // 최고 속도
    public String name; // 차 이름
    public int wheel; // 바퀴 수
    public String color; // 색상

    Car(String brand, double topSpeed, String name, int wheel, String color) {
        this.brand = brand;
        this.topSpeed = topSpeed;
        this.name = name;
        this.wheel = wheel;
        this.color = color;
    }

}
